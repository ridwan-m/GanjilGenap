public class OperatorPerbandingan{
    public static void main(String[] args) {
        int a = 10;
        int b = 15;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        System.out.println(a == b);
        System.out.println(a != b);

        String username = "admin";
        System.out.println(username == "ridwan");
        System.out.println(username != "ridwan");
    }
}